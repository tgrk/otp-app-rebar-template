-module({{short}}).

%% Application API
-export([
    start/0,
    stop/0
]).

-include("{{short}}.hrl").

%%%============================================================================
%%% API
%%%============================================================================


%%%===========================================================================
%%% Application API
%%%===========================================================================
start() ->
    [ensure_started(D) || D <- deps() ++ [?APP]],
    ok.

stop() ->
    [application:stop(D) || D <- deps()],
    ok.

%%%============================================================================
%%% Internal functionality
%%%============================================================================
deps() ->
    [lager].

ensure_started(App) ->
    case application:start(App) of
        ok ->
            ok;
        {error, {already_started, App}} ->
            ok;
        {error, _} = Error ->
            Error
    end.
